impl PrettyRow for UnicodeBlock {
    fn to_format() -> prettytable::format::TableFormat {
        *prettytable::format::consts::FORMAT_CLEAN
    }

    fn to_head() -> prettytable::Row {
        row![ Fy => "Name", "CJK", "Start", "End" ]
    }

    fn to_row(&self) -> prettytable::Row {
        row![
            self.name(),
            unicode_blocks::is_cjk_block(*self),
            self.end() - self.start(),
            format!("{:6x}", self.start()),
            format!("{:6x}", self.end())
        ]
    }
}
